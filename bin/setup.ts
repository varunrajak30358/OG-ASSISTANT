#!/usr/bin/env node
/**
 * OG Assistant — First-run setup wizard
 * Runs before dev/start. If API key already exists in .env, skips silently.
 */
process.env.NODE_NO_WARNINGS = "1";

import { confirm, input, select } from "@inquirer/prompts";
import * as fs from "fs";
import * as path from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname  = path.dirname(__filename);
const ENV_PATH   = path.resolve(__dirname, "../.env");

// ── colors ────────────────────────────────────────────────────────────────────
const g  = (s: string) => `\x1b[38;2;0;200;255m${s}\x1b[0m`;
const dg = (s: string) => `\x1b[38;2;0;130;180m${s}\x1b[0m`;
const cy = (s: string) => `\x1b[38;2;0;210;180m${s}\x1b[0m`;
const w  = (s: string) => `\x1b[97m${s}\x1b[0m`;
const d  = (s: string) => `\x1b[2m${s}\x1b[0m`;
const pu = (s: string) => `\x1b[38;2;160;100;255m${s}\x1b[0m`;
const rd = (s: string) => `\x1b[31m${s}\x1b[0m`;

const LINE = "─".repeat(54);
const ln   = () => console.log();
const div  = (label = "") => {
  if (!label) { console.log(dg(`  ${LINE}`)); return; }
  const pad = Math.floor((54 - label.length - 2) / 2);
  console.log(dg(`  ${"─".repeat(pad)} ${label} ${"─".repeat(54 - pad - label.length - 2)}`));
};
const ok   = (msg: string) => console.log(`  ${g("✓")}  ${msg}`);
const hint = (msg: string) => console.log(`  ${cy("·")}  ${d(msg)}`);
const info = (msg: string) => console.log(`  ${dg("›")}  ${d(msg)}`);

const sleep = (ms: number) => new Promise<void>((r) => setTimeout(r, ms));

async function spinner(label: string, ms = 600) {
  const frames = ["⠋","⠙","⠹","⠸","⠼","⠴","⠦","⠧","⠇","⠏"];
  const end = Date.now() + ms;
  let i = 0;
  while (Date.now() < end) {
    process.stdout.write(`\r  ${g(frames[i % frames.length])}  ${d(label)}`);
    await sleep(60);
    i++;
  }
  process.stdout.write(`\r  ${g("✓")}  ${d(label)}\n`);
}

// ── read current .env ─────────────────────────────────────────────────────────
function readEnv(): Record<string, string> {
  if (!fs.existsSync(ENV_PATH)) return {};
  const lines = fs.readFileSync(ENV_PATH, "utf-8").split("\n");
  const map: Record<string, string> = {};
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const idx = trimmed.indexOf("=");
    if (idx === -1) continue;
    map[trimmed.slice(0, idx).trim()] = trimmed.slice(idx + 1).trim();
  }
  return map;
}

// ── write back to .env ────────────────────────────────────────────────────────
function writeEnv(values: Record<string, string>) {
  let content = "# OG Assistant Environment Configuration\n";
  content += "# Auto-generated by setup wizard — edit manually if needed\n\n";
  for (const [k, v] of Object.entries(values)) {
    content += `${k}=${v}\n`;
  }
  fs.writeFileSync(ENV_PATH, content, "utf-8");
}

// ── banner ────────────────────────────────────────────────────────────────────
function printBanner() {
  console.clear();
  ln();
  console.log(g("   ██████╗  ██████╗ "));
  console.log(g("  ██╔═══██╗██╔════╝ "));
  console.log(g("  ██║   ██║██║  ███╗"));
  console.log(g("  ██║   ██║██║   ██║"));
  console.log(g("  ╚██████╔╝╚██████╔╝"));
  console.log(g("   ╚═════╝  ╚═════╝ "));
  console.log(w("  A S S I S T A N T"));
  ln();
  console.log(dg(`  ${LINE}`));
  console.log(`  ${cy("SETUP")}   ${d("·")}   ${d("First-time configuration")}   ${d("·")}   ${pu("Powered by Gemini")}`);
  console.log(dg(`  ${LINE}`));
  ln();
}

// ── main ──────────────────────────────────────────────────────────────────────
async function main() {
  const env = readEnv();

  // Already configured — skip silently
  if (env.GOOGLE_API_KEY && env.GOOGLE_API_KEY.length > 10) {
    process.exit(0);
  }

  // First time — show wizard
  printBanner();
  console.log(`  ${dg("Welcome!")}  ${d("Looks like your first time here — let's get you set up.")}`);
  ln();

  div("  STEP 1 / 2  ·  API KEY  ");
  ln();
  const keyUrl = "https://aistudio.google.com/app/api-keys";
  console.log(`  ${cy("·")}  ${d("Get your FREE Gemini key at:")}`);
  console.log(`  ${cy("·")}  ${g(keyUrl)}`);
  ln();
  hint("It's free — no credit card needed.");
  ln();

  const apiKey = await input({
    message: g("  Paste your Gemini API key"),
    validate: (v) => {
      if (!v.trim()) return "API key cannot be empty.";
      if (v.trim().length < 15) return "That doesn't look like a valid key — paste the full key.";
      return true;
    },
    transformer: (v) => v.trim(),
  });

  ln();
  await spinner("Validating key", 500);
  ln();

  div("  STEP 2 / 2  ·  VOICE  ");
  ln();
  hint("You can change this anytime by editing the .env file.");
  ln();

  const voice = await select({
    message: g("  Choose assistant voice"),
    choices: [
      { name: `${w("Aoede")}   ${d("Female  ·  light, melodic")}`,      value: "Aoede",  short: "Aoede"  },
      { name: `${w("Kore")}    ${d("Female  ·  clear, professional")}`,  value: "Kore",   short: "Kore"   },
      { name: `${w("Zephyr")}  ${d("Female  ·  airy, casual")}`,         value: "Zephyr", short: "Zephyr" },
      { name: `${w("Puck")}    ${d("Male    ·  deep, calm")}`,           value: "Puck",   short: "Puck"   },
      { name: `${w("Charon")}  ${d("Male    ·  smooth, neutral")}`,      value: "Charon", short: "Charon" },
      { name: `${w("Fenrir")}  ${d("Male    ·  medium, balanced")}`,     value: "Fenrir", short: "Fenrir" },
    ],
  });

  ln();
  div("  REVIEW  ");
  ln();
  info(`API key  →  ${"•".repeat(Math.max(0, apiKey.length - 6))}${apiKey.slice(-6)}`);
  info(`Voice    →  ${voice}`);
  info(`Saved to →  .env`);
  ln();

  const confirmed = await confirm({
    message: g("  Save and start OG Assistant?"),
    default: true,
  });

  if (!confirmed) {
    ln();
    hint("No problem. Run  npm run dev  again whenever you're ready.");
    ln();
    process.exit(0);
  }

  ln();
  await spinner("Saving configuration to .env", 400);

  // Merge with existing env values (preserve any other keys)
  env.GOOGLE_API_KEY = apiKey;
  env.OG_VOICE       = voice;
  writeEnv(env);

  ok(`Configuration saved to  ${d(".env")}`);
  ln();
  div();
  ln();
  console.log(`  ${g("✓")}  ${w("All set! Starting OG Assistant...")}`);
  ln();

  // Small pause so user can read
  await sleep(800);
  process.exit(0);
}

main().catch((err: Error) => {
  // User pressed Ctrl+C during prompts — exit cleanly
  if (err.message?.includes("User force closed")) {
    ln();
    hint("Setup cancelled. Run  npm run dev  again to restart.");
    ln();
    process.exit(0);
  }
  ln();
  console.log(`  ${rd("✗")}  Setup failed: ${d(err.message)}`);
  ln();
  process.exit(1);
});
